package fengfei.studio.datajob;

public class SchoolCenter {
    private String schoolName;
    private String schoolSymbol;
    private String centerName;
    private String centerAddr;
    private String centerManager;
    private String centerPhone;
    private String centerEmail;
    private String centerZIP;
    private String centerIntro;

    public String getSchoolName() {
        return schoolName;
    }

    public void setSchoolName(String schoolName) {
        this.schoolName = schoolName;
    }

    public String getSchoolSymbol() {
        return schoolSymbol;
    }

    public void setSchoolSymbol(String schoolSymbol) {
        this.schoolSymbol = schoolSymbol;
    }

    public String getCenterName() {
        return centerName;
    }

    public void setCenterName(String centerName) {
        this.centerName = centerName;
    }

    public String getCenterAddr() {
        return centerAddr;
    }

    public void setCenterAddr(String centerAddr) {
        this.centerAddr = centerAddr;
    }

    public String getCenterManager() {
        return centerManager;
    }

    public void setCenterManager(String centerManager) {
        this.centerManager = centerManager;
    }

    public String getCenterPhone() {
        return centerPhone;
    }

    public void setCenterPhone(String centerPhone) {
        this.centerPhone = centerPhone;
    }

    public String getCenterEmail() {
        return centerEmail;
    }

    public void setCenterEmail(String centerEmail) {
        this.centerEmail = centerEmail;
    }

    public String getCenterZIP() {
        return centerZIP;
    }

    public void setCenterZIP(String centerZIP) {
        this.centerZIP = centerZIP;
    }

    public String getCenterIntro() {
        return centerIntro;
    }

    public void setCenterIntro(String centerIntro) {
        this.centerIntro = centerIntro;
    }
}

